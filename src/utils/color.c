#include "color.h"

#include <math.h>

int	color_to_rgb(const t_color *c)
{
	int	r;
	int	g;
	int	b;

	r = fmin(fmax(c->r * 255.0, 0.0), 255.0);
	g = fmin(fmax(c->g * 255.0, 0.0), 255.0);
	b = fmin(fmax(c->b * 255.0, 0.0), 255.0);
	return (r << 16 | g << 8 | b);
}
